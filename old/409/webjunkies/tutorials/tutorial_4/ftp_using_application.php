<div class="tutorialParagraph">
Using an FTP application can be preferable to some, as they feel more comfortable using the graphical interface than a text-based one.  Two good FTP applications are 'fetch' for the mac, and SmartFtp for windows.  Here we will focus on smartftp.  SmartFtp is very convenient and easy to use, and has a lot of information available at your fingertips.  When you load up Smartftp for the first time, there are 4 main areas that you should know about. <br /><br />


1. At the very top, there is a toolbar that contains your login information. When you first use the program, it will be empty.  On the left, you enter what server you want to access, then to the right of that, your user ID and password.  The first time the program loads, or if you don't enter anything, you will be assumed as anonymous.<br /><br />
2. Just below that, there is a tab that states what server you are connected to with a browser window right underneath it.  Here, you can easily navigate through the folders within your server without having to use command lines to navigate.<br /><br />
3. The session log is right underneath that.  Here you can see what is happening with your session. When you login successfully, it is verified here. If your login was unsuccessful for some reason, the reasons will be displayed here. Anything that happens during your session is recorded here for you to see.<br /><br />
4. The taskchart at the bottom displays what files are being transferred and what their status is.
From here it is a simple matter of click and drag to the appropriate directory to upload or download files.  To close the session, just close the tab.<br /><br />
