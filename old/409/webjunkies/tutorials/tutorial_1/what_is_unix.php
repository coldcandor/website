	<div class="tutorialParagraph">
	UNIX is a computer operating system first developed at Bell Labs (and, to get the legal info out of the way, a trademark of AT&amp;T Bell Laboratories). <br /><br />
	An "operating system" is essentially what runs the computer.  It coordinates all the programs running on the computer, as well as input (such as the keyboard and mouse) and output (such as the monitor).<br /><br />
	UNIX is based on a collection of small, easily understood utilities.  In and of itself, each is a useful program.  They also allow you to connect them in many different ways (including ways that the authors did not predict), building procedures and sophisticated tasks to suit your own needs. <br /><br />
	The power and flexibility of UNIX does not come without a cost however; many people find UNIX systems extremely unfriendly. Commands and responses are not only terse -- some are positively opaque. Still, if you are willing to put up with a certain amount of difficulty, you may find that the power of UNIX is well worth your time and effort.<br /><br />
