<div class="tutorialParagraph">As previously stated, you can only add text to your document while in edit mode, and VI has several different ways to reach edit mode, depending on how you want to edit the text.  The following commands will get you to edit mode. Be careful, VI is case sensitive.<br /><br />
 <em>a : add text after the cursor<br />
A : add text at the end of the line<br />
c : begin change operation<br />
C : change at the end of the line<br />
i : add new text before the cursor<br />
I : add text at the beginning of the line<br />
o : opens a line below the current one<br />
O : opens a line above the current one<br />
R : begin overwriting<br />
s : substitute a character<br />
S : substitute a line</em><br /><br />
You can hit the <em>escape</em> key to get back to command mode.

