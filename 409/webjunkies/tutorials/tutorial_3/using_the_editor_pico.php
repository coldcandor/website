^J : justify the curent paragraph<br />
^I : insert a tab at the current location<br />
^T : spell checker<br />
^C : print the current cursor location<br />
^W or F6 : search for text<br />
^L : redraw screen<br />
^G or F1 : display the help file within Pico<br />
^R or F5 : read in file at the current location</em><br /><br />

